#'
#' @examplesIf interactive()
#' plot(g,
#'   vertex.shape = "pie", vertex.pie = values,
#'   vertex.pie.color = list(heat.colors(5)),
#'   vertex.size = seq(10, 30, length.out = 10), vertex.label = NA
